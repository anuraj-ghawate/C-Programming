#include<stdio.h>

int main()
{
    char ch='A';
    int i=10;
    float f=3.14f;
    double d=12.3456543;

    printf("Value Of ch is: %c\n",ch);
    printf("Value Of i is: %d\n",i);
    printf("Value Of float is: %f\n",f);
    printf("Value Of double is: %lf\n",d);


    return 0;
}